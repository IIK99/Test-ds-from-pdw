# list
list = [1, 2, 3, "hello python", True]
print(list, type(list))

# tuple list
tuple = (1, 2, 3, "hello python", True)
print(tuple, type(tuple))

# set list
set = {1, 1, 2, 2, 3, 3, "hello python", True}
print(set, type(set))

# dictionary
dictionary = {"name": "Joan", "age": 25}
print(dictionary, type(dictionary))
